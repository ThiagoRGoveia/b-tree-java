//  Class for keys inside node
public class TreeNodeData {
    private int key;

    // Constructor
    public TreeNodeData(int val) {
        this.key = val;
    }

    // Setter method for key
    public void setKey(int key) {
        this.key = key;
    }

    // Getter method for key
    public int getKey() {
        return key;
    }
}
